const WOW_KEY_BY_KEY_ID: Record<string, string> = {
  Backquote: '`',
  Minus: '-',
  Equal: '=',
  Tab: 'TAB',
  CapsLock: 'CAPSLOCK',
  Space: 'SPACE',
  BracketLeft: '[',
  BracketRight: ']',
  Semicolon: ';',
  Quote: "'",
  Comma: ',',
  Period: '.',
  Slash: '/',
  Mouse4: 'BUTTON4',
  Mouse5: 'BUTTON5',
  WheelUp: 'MOUSEWHEELUP',
  WheelDown: 'MOUSEWHEELDOWN',
}

function wowKeyForKeyId(keyId: string): string {
  const mapped = WOW_KEY_BY_KEY_ID[keyId]
  if (mapped !== undefined) return mapped
  if (keyId.startsWith('Key')) return keyId.slice(3)
  if (keyId.startsWith('Digit')) return keyId.slice(5)
  if (keyId.startsWith('MouseG')) return `BUTTON${Number(keyId.slice(6)) + 5}`
  return keyId.toUpperCase()
}

export function wowKeyName(slot: Slot): string {
  const key = wowKeyForKeyId(slot.keyId)
  const prefix =
    slot.modifier === 'shift'
      ? 'SHIFT-'
      : slot.modifier === 'ctrl'
        ? 'CTRL-'
        : slot.modifier === 'alt'
          ? 'ALT-'
          : ''
  return `${prefix}${key}`
}

export interface AddonKeyboardKey {
  k: string
  label: string
  x: number
  w: number
  m?: boolean
}

export type AddonKeyboardRow = AddonKeyboardKey[]

export function buildAddonKeyboard(hardware: HardwareConfig): AddonKeyboardRow[] {
  const scheme = MOVEMENT_SCHEMES[hardware.movementScheme]
  const movement = new Set(scheme.movementKeyIds)
  const banned = new Set(hardware.bannedKeyIds)
  const keys = buildKeyboardGeometry(hardware.formFactor, hardware.layout)
  const rowsByY = new Map<number, AddonKeyboardRow>()
  for (const key of keys) {
    const row = rowsByY.get(key.y) ?? []
    row.push({
      k: wowKeyForKeyId(key.id),
      label: key.label,
      x: Math.round(key.x * 10),
      w: Math.round(key.w * 10),
      m: movement.has(key.id) || banned.has(key.id) || !isBindableKey(key) || undefined,
    })
    rowsByY.set(key.y, row)
  }
  const rows = [...rowsByY.entries()]
    .sort((a, b) => a[0] - b[0])
    .map(([, row]) => row.sort((a, b) => a.x - b.x))
  const mouseButtons = [
    ...MOUSE_BUTTONS[hardware.mouse],
    ...(hardware.includeMouseWheel ? WHEEL_BUTTONS : []),
  ].filter((button) => !banned.has(button.id))
  if (mouseButtons.length > 0) {
    rows.push(
      mouseButtons.map((button, index) => ({
        k: wowKeyForKeyId(button.id),
        label: button.label,
        x: index * 12,
        w: 10,
      })),
    )
  }
  return rows
}

export interface ExportBind {
  ability: Ability
  slot: Slot
  name: string
  wowKey: string
}

const MODIFIER_RANK: Record<string, number> = { none: 0, shift: 1, ctrl: 2, alt: 3 }

function bindOrder(bind: ExportBind): [number, number] {
  return [
    MODIFIER_RANK[bind.slot.modifier] ?? 9,
    KEY_POSITION_INDEX.get(bind.slot.keyId) ?? 999,
  ]
}

function compareBinds(a: ExportBind, b: ExportBind): number {
  const [modA, keyA] = bindOrder(a)
  const [modB, keyB] = bindOrder(b)
  return modA - modB || keyA - keyB
}

export function buildExportBinds(
  assignments: BindAssignment[],
  abilities: Ability[],
  slots: Slot[],
  spells: SpellTextShard,
  labels: ZeroSpellLabels,
): ExportBind[] {
  const abilityById = new Map(abilities.map((ability) => [ability.id, ability]))
  const slotById = new Map(slots.map((slot) => [slot.id, slot]))
  const binds: ExportBind[] = []
  for (const assignment of assignments) {
    const ability = abilityById.get(assignment.abilityId)
    const slot = slotById.get(assignment.slotId)
    if (!ability || !slot) continue
    const name =
      ability.spellId === 0
        ? zeroSpellLabel(ability.id, labels)
        : (spells[String(ability.spellId)]?.name ?? `#${ability.spellId}`)
    binds.push({ ability, slot, name, wowKey: wowKeyName(slot) })
  }
  return binds.sort(compareBinds)
}

export function renderPlainList(binds: ExportBind[]): string {
  const width = Math.max(...binds.map((bind) => bind.wowKey.length), 4)
  return binds
    .map((bind) => {
      const variant =
        bind.ability.variantKind === 'base' || bind.ability.category === 'targeting'
          ? ''
          : ` [@${variantTarget(bind.ability.variantKind)}]`
      return `${bind.wowKey.padEnd(width)}  ${bind.name}${variant}`
    })
    .join('\n')
}

function variantTarget(kind: string): string {
  if (kind === 'focus') return 'focus'
  if (kind === 'arena1') return 'arena1'
  if (kind === 'arena2') return 'arena2'
  if (kind === 'arena3') return 'arena3'
  return kind
}

const TARGETING_COMMANDS: Record<string, string> = {
  'target:arena1': 'TARGETARENA1',
  'target:arena2': 'TARGETARENA2',
  'target:arena3': 'TARGETARENA3',
}

const SET_FOCUS_MACRO = '/focus [@mouseover,exists][]'

export function macroName(bind: ExportBind): string {
  const target = variantTarget(bind.ability.variantKind)
  const compactName = bind.name.replace(/[^\p{L}\p{N}]/gu, '').slice(0, 12)
  return `KO${target === 'base' ? '' : target[0]?.toUpperCase()}${target.slice(1, 3)}${compactName}`.slice(0, 16)
}

export function macroBody(bind: ExportBind): string | null {
  const kind = bind.ability.variantKind
  if (bind.ability.id === 'focus:set') return SET_FOCUS_MACRO
  if (bind.ability.category === 'targeting') return null
  if (kind === 'focus') return `#showtooltip ${bind.name}\n/cast [@focus,exists][] ${bind.name}`
  if (kind === 'arena1' || kind === 'arena2' || kind === 'arena3') {
    return `#showtooltip ${bind.name}\n/cast [@${kind}] ${bind.name}`
  }
  if (bind.ability.targeting === 'ally' && bind.ability.spellId > 0) {
    return `#showtooltip ${bind.name}\n/cast [@mouseover,help,nodead][help][@player] ${bind.name}`
  }
  if (bind.ability.id === 'trinket:1') return `/use 13`
  if (bind.ability.id === 'trinket:pvp') return `/use 14`
  return null
}

export function renderMacroList(binds: ExportBind[]): string {
  const sections: string[] = []
  for (const bind of binds) {
    const body = macroBody(bind)
    if (body === null) continue
    sections.push(`-- ${macroName(bind)} (${bind.wowKey})\n${body}`)
  }
  return sections.join('\n\n')
}

export interface LuaBindEntry {
  key: string
  spell?: number
  item?: number
  command?: string
  macrotext?: string
  name?: string
  target?: string
  mouseover?: boolean
  category?: string
  variant?: string
  slot?: number
}

export interface LuaBindPlacement {
  bind: ExportBind
  entry: LuaBindEntry
}

export function buildLuaBindPlacements(
  binds: ExportBind[],
  decor?: AddonDecor,
): LuaBindPlacement[] {
  const placements: LuaBindPlacement[] = []
  const entries: LuaBindEntry[] = []
  const planned: PlannedBind[] = []

  for (const bind of binds) {
    const entry: LuaBindEntry = { key: bind.wowKey }
    if (bind.ability.id === 'trinket:1') {
      entry.item = 13
      entry.name = bind.name
    } else if (bind.ability.id === 'trinket:pvp') {
      entry.item = 14
      entry.name = bind.name
    } else if (TARGETING_COMMANDS[bind.ability.id]) {
      entry.command = TARGETING_COMMANDS[bind.ability.id]
      entry.name = bind.name
    } else if (bind.ability.id === 'focus:set') {
      entry.macrotext = SET_FOCUS_MACRO
      entry.name = bind.name
    } else if (bind.ability.spellId > 0) {
      entry.spell = bind.ability.spellId
      if (bind.ability.variantKind === 'focus') entry.target = 'focus'
      else if (bind.ability.variantKind.startsWith('arena')) entry.target = bind.ability.variantKind
      else if (bind.ability.targeting === 'ally') entry.mouseover = true
    } else {
      continue
    }

    if (decor) {
      entry.category = bind.ability.category
      if (bind.ability.variantKind !== 'base' && bind.ability.category !== 'targeting') {
        entry.variant = variantTarget(bind.ability.variantKind)
      }
    }

    entries.push(entry)
    placements.push({ bind, entry })
    planned.push({
      keyId: bind.slot.keyId,
      modifier: bind.slot.modifier,
      placeable: entry.command === undefined,
    })
  }

  const plan = buildPlacementPlan(planned)
  entries.forEach((entry, index) => {
    const slot = plan[index]
    if (slot !== null && slot !== undefined) entry.slot = slot
  })
  return placements
}

export function buildLuaBindEntries(binds: ExportBind[], decor?: AddonDecor): LuaBindEntry[] {
  return buildLuaBindPlacements(binds, decor).map((placement) => placement.entry)
}

export interface AddonProfile {
  id: string
  name: string
  mode: GameMode
  specId: number
  classTag: string
  hash: string
  binds: LuaBindEntry[]
  keyboard: AddonKeyboardRow[]
}

function fnv1a(input: string): string {
  let hash = 0x811c9dc5
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i)
    hash = Math.imul(hash, 0x01000193) >>> 0
  }
  return hash.toString(16).padStart(8, '0')
}

export function buildAddonProfile(
  id: string,
  name: string,
  mode: GameMode,
  specId: number,
  classTag: string,
  binds: ExportBind[],
  keyboard: AddonKeyboardRow[],
  decor?: AddonDecor,
): AddonProfile {
  const entries = buildLuaBindEntries(binds, decor)
  const signature = entries
    .map(
      (entry) =>
        `${entry.key}|${entry.spell ?? ''}|${entry.item ?? ''}|${entry.command ?? ''}|${entry.macrotext ?? ''}|${entry.target ?? ''}|${entry.mouseover ? 1 : 0}|${entry.slot ?? ''}`,
    )
    .join(';')
  return {
    id,
    name,
    mode,
    specId,
    classTag,
    hash: fnv1a(`${specId}:${signature}`),
    binds: entries,
    keyboard,
  }
}

function luaBindLiteral(entry: LuaBindEntry): string {
  const parts = [`key = "${entry.key}"`]
  if (entry.spell !== undefined) parts.push(`spell = ${entry.spell}`)
  if (entry.item !== undefined) parts.push(`item = ${entry.item}`)
  if (entry.command !== undefined) parts.push(`command = "${entry.command}"`)
  if (entry.macrotext !== undefined) parts.push(`macrotext = "${escapeLua(entry.macrotext)}"`)
  if (entry.name !== undefined) parts.push(`name = "${escapeLua(entry.name)}"`)
  if (entry.target !== undefined) parts.push(`target = "${entry.target}"`)
  if (entry.mouseover) parts.push(`mouseover = true`)
  if (entry.category !== undefined) parts.push(`category = "${entry.category}"`)
  if (entry.variant !== undefined) parts.push(`variant = "${entry.variant}"`)
  if (entry.slot !== undefined) parts.push(`slot = ${entry.slot}`)
  return `      { ${parts.join(', ')} },`
}

function escapeLua(value: string): string {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n')
}

function buildLegendCategories(profiles: AddonProfile[]): string[] {
  const seen = new Set<string>()
  const categories: string[] = []
  for (const profile of profiles) {
    for (const entry of profile.binds) {
      if (!entry.category || seen.has(entry.category)) continue
      seen.add(entry.category)
      categories.push(entry.category)
    }
  }
  return categories
}

function localeStringsLiteral(strings: AddonLocaleStrings, colorKeys: string[]): string[] {
  const categoryLines = colorKeys.map(
    (key) => `      ["${key}"] = "${escapeLua(strings.categories[key as AbilityCategory] ?? key)}",`,
  )
  const uiLines = ADDON_UI_KEYS.map(
    (key) => `      ${key} = "${escapeLua(strings.ui[key])}",`,
  )
  return [`    categories = {`, ...categoryLines, `    },`, `    ui = {`, ...uiLines, `    },`]
}

