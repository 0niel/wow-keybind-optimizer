function keyboardRowLiteral(row: AddonKeyboardRow): string {
  const keys = row.map((key) => {
    const parts = [`k = "${escapeLua(key.k)}"`, `label = "${escapeLua(key.label)}"`, `x = ${key.x}`, `w = ${key.w}`]
    if (key.m) parts.push(`m = true`)
    return `{ ${parts.join(', ')} }`
  })
  return `    { ${keys.join(', ')} },`
}

